    private void tampil_data(){
        try{
            Object[] judul_kolom = {"Command", "Reply", "Information"};
            tabModel=new DefaultTableModel(null,judul_kolom);
            tblCommand.setModel(tabModel);
            
            Connection conn=(Connection)KoneksiMySql.koneksiDB();
            Statement stt=conn.createStatement();
            tabModel.getDataVector().removeAllElements();
            
            RsProduk=stt.executeQuery("SELECT * from keyword");  
            while(RsProduk.next()){
                Object[] data={
                    RsProduk.getString("command"),
                    RsProduk.getString("reply"),        
                    RsProduk.getString("keterangan")        
                };
               tabModel.addRow(data);
            }                
        } catch (Exception ex) {
        System.err.println(ex.getMessage());
        }
        // disable button
        btnSimpan.setEnabled(false);
        btnKoreksi.setEnabled(false);
        btnHapus.setEnabled(false);
        btnBatal.setEnabled(false);
        btnKeluar.setEnabled(true);
    }
    
    void tbl_keForm(){
        txtCommand.setText(tabModel.getValueAt(tblCommand.getSelectedRow(),0)+"");
        txtReply.setText(tabModel.getValueAt(tblCommand.getSelectedRow(),1)+"");
        txtInfo.setText(tabModel.getValueAt(tblCommand.getSelectedRow(),2)+"");
        
        btnKoreksi.setEnabled(true);
        btnHapus.setEnabled(true);
        btnBatal.setEnabled(true);
        btnKeluar.setEnabled(true);
        btnSimpan.setEnabled(false);
    }
    
    private void bersih_data(){
        txtCommand.setText("");
        txtReply.setText("");
        txtInfo.setText("");
    } 
    
    private void set_editOff(){
        txtCommand.setEnabled(false);
        txtReply.setEnabled(false);
        txtInfo.setEnabled(false);
    }
    
    private void set_editOn(){
        txtCommand.setEnabled(true);
        txtReply.setEnabled(true);
        txtInfo.setEnabled(true);
   }
    
