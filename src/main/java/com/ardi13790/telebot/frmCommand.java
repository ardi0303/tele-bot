    private void btnKoreksiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKoreksiActionPerformed
        // TODO add your handling code here:
        btnTambah.setEnabled(true);
        btnKeluar.setEnabled(true);
        btnSimpan.setEnabled(false);
        btnKoreksi.setEnabled(false);
        btnHapus.setEnabled(false);
        btnBatal.setEnabled(false);
        
        String tCommand=txtCommand.getText();
        String tReply=txtReply.getText();
        String tInfo=txtInfo.getText();
        
        if (tCommand.isEmpty() ) {
            JOptionPane.showMessageDialog(null,"Command masih kosong!");
            txtCommand.requestFocus();
        }else if (tReply.isEmpty()) {
            JOptionPane.showMessageDialog(null,"Reply masih kosong!");
            txtReply.requestFocus();
        }else if (tInfo.isEmpty()) {
            JOptionPane.showMessageDialog(null,"Info masih kosong!");
            txtInfo.requestFocus();
        }else{
            try{
                Connection conn=(Connection)KoneksiMySql.koneksiDB();
                Statement stt=conn.createStatement();
                stt.executeUpdate("UPDATE keyword SET command='"+tCommand+"', reply='"+tReply+"' , keterangan='"+tInfo+"' "
                        + "WHERE command='"+tCommand+"'");
                bersih_data();
                tampil_data();
                set_editOff();
                JOptionPane.showMessageDialog(this,"Data berhasil diubah","Success",JOptionPane.INFORMATION_MESSAGE);
            } catch(SQLException e){
                JOptionPane.showMessageDialog(this,"Ubah data gagal\n"+e.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnKoreksiActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        btnTambah.setEnabled(true);
        btnKeluar.setEnabled(true);
        btnSimpan.setEnabled(false);
        btnKoreksi.setEnabled(false);
        btnHapus.setEnabled(false);
        btnBatal.setEnabled(false);
        
        String tCommand=txtCommand.getText();

        if (tCommand.isEmpty() ) {
            JOptionPane.showMessageDialog(null,"Command tidak boleh kosong!");
            txtCommand.requestFocus();
        }else if(JOptionPane.showConfirmDialog(null,"Apakah anda yakin akan menghapus data ini?",
            "Informasi",JOptionPane.OK_CANCEL_OPTION,JOptionPane.INFORMATION_MESSAGE)==JOptionPane.OK_OPTION){
            try{
                Connection conn=(Connection)KoneksiMySql.koneksiDB();
                Statement stt=conn.createStatement();
                stt.executeUpdate("DELETE FROM keyword WHERE command='"+tCommand+"'");
                bersih_data();
                tampil_data();
                set_editOff();
                JOptionPane.showMessageDialog(this,"Data berhasil dihapus","Success",JOptionPane.INFORMATION_MESSAGE);
            } catch(SQLException e){
                JOptionPane.showMessageDialog(this,"Data gagal dihapus\n"+e.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnHapusActionPerformed

